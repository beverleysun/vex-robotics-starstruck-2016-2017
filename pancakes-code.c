task usercontrol()
{
    //forever loop
    while (true)
    {
        //drive with left joystick via addition of vectors
        motor[leftBmotor]  =  vexRT[Ch3] + vexRT[Ch4];
        motor[rightBmotor] =  vexRT[Ch3] - vexRT[Ch4];
    
        //strafe wheel using right joystick
        motor[strafe] = vexRT[Ch1];

        //arm motors controlled by buttons 6U and 6D
        if(vexRT[Btn6U] == 1)
        {
            //arm up
            motor[rightArm]= 127;
            motor[leftArm]= 127;
        }
        else
        {
            //arm down
            if(vexRT[Btn6D] == 1)
            {
                motor[rightArm]= -127;
                motor[leftArm]= -127;
            }
            else
            {
                //stay still
                motor[rightArm]= 0;
                motor[leftArm]= 0;
            }
        }

        //claw motors controlled by buttons 8R and 8L
        if(vexRT[Btn8R] == 1)
        {
            //open claw
            motor[Claw]= 50;
        }
        else
        {
            if(vexRT[Btn8L] == 1)
            {
                //close claw
                motor[Claw]= -50;
            }
            else
            {
                //just enough power to grip but not enough to close claw
                motor[Claw]= -10;
            }
        }
    }
}
